  const SUPPORTED = ['nl', 'en', 'de'];

  function getLang() {
    const s = localStorage.getItem('lang');
    return SUPPORTED.includes(s) ? s : 'en';
  }

  function setLang(lang) {
    if (!SUPPORTED.includes(lang)) return;
    localStorage.setItem('lang', lang);
    location.reload();
  }

  function t(key, vars) {
    const lang = getLang();
    const dict = TRANSLATIONS[lang] || TRANSLATIONS.nl;
    let str = Object.prototype.hasOwnProperty.call(dict, key) ? dict[key]
            : Object.prototype.hasOwnProperty.call(TRANSLATIONS.nl, key) ? TRANSLATIONS.nl[key]
            : key;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), v);
      }
    }
    return str;
  }

  function applyI18n() {
    const lang = getLang();
    document.documentElement.lang = lang;
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      el.innerHTML = t(el.dataset.i18nHtml);
    });
  }

  window.t = t;
  window.setLang = setLang;
  window.getLang = getLang;
  window.applyI18n = applyI18n;
  window.MAANDEN_KORT = (TRANSLATIONS[getLang()] || TRANSLATIONS.nl).months;

  document.addEventListener('DOMContentLoaded', applyI18n);
